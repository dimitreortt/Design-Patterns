export class GarageDoor {
    constructor() { }

    up() {
        console.log("Garage door is opening");
    }
    
    down() {
        console.log("Garage door is closing");
    }
    
    stop() {
        console.log("Garage door is stopped");
    }
    
    lightOn() {
        console.log("Garage door is light is on");
    }
    
    lightOff() {
        console.log("Garage door is light is off");
    }
}
